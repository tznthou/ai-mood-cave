/**
 * 根據當前環境動態獲取 API URL
 */
function getApiUrl() {
    const currentPort = window.location.port;
    
    // 如果在 3001 端口（Node.js 服務器），使用相對路徑
    if (currentPort === '3001') {
        return '/api/ai/chat';
    }
    
    // 如果在其他端口（如 3000 Python 服務器），使用 3001 端口
    return 'http://localhost:3001/api/ai/chat';
}

